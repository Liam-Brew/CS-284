package shapes;

public class PairDoubleDouble {
	// Data fields
	private double x;
	private double y;
	
	public PairDoubleDouble(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
